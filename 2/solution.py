ranges = [[0, 0] for _ in range(len(input))]

# Parse ranges
for i in range(len(input)):
    edges = input[i].split("-")

    ranges[i][0] = edges[0]
    ranges[i][1] = edges[1]

invalid_nums = []

for item in ranges:
    for num in range(int(item[0]), int(item[1]) + 1):
        num = str(num)
        length = len(num)

        if length % 2 == 0:
            half = length // 2

            first_part = num[:half]
            second_part = num[half:]
            
            if first_part == second_part:
                invalid_nums.append(int(first_part + second_part))

print(sum(invalid_nums))